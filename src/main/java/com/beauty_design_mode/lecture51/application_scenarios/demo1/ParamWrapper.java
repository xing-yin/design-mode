package com.beauty_design_mode.lecture51.application_scenarios.demo1;

/**
 * 参数包装类(辅助类，可忽略)
 *
 * @author Alan Yin
 * @date 2020/8/13
 */

public class ParamWrapper {

    private int paramA;
    private int paramB;
    private int paramC;

    public int getParamA() {
        return paramA;
    }

    public void setParamA(int paramA) {
        this.paramA = paramA;
    }

    public int getParamB() {
        return paramB;
    }

    public void setParamB(int paramB) {
        this.paramB = paramB;
    }

    public int getParamC() {
        return paramC;
    }

    public void setParamC(int paramC) {
        this.paramC = paramC;
    }
}
